def check_table_exists(conn: sqlite3.Connection, table_name: str) -> bool:
    """Check if a table exists in the database."""
    cursor = conn.execute(
        "SELECT name FROM sqlite_master WHERE type='table' AND name=?",
        (table_name,)
    )
    return cursor.fetchone() is not None

def check_column_exists(conn: sqlite3.Connection, table_name: str, column_name: str) -> bool:
    """Check if a column exists in a table."""
    cursor = conn.execute(f"PRAGMA table_info({table_name})")
    columns = [row[1] for row in cursor.fetchall()]
    return column_name in columns

def check_migration_applied(conn: sqlite3.Connection, version: str, name: str) -> bool:
    """Check if a migration has already been applied."""
    if not check_table_exists(conn, 'migration_history'):
        return False
    
    cursor = conn.execute(
        "SELECT id FROM migration_history WHERE version = ? AND name = ? AND success = 1",
        (version, name)
    )
    return cursor.fetchone() is not None

def record_migration(conn: sqlite3.Connection, version: str, name: str, description: str):
    """Record a successful migration."""
    conn.execute(
        """INSERT INTO migration_history (version, name, description, applied_by)
           VALUES (?, ?, ?, ?)""",
        (version, name, description, 'migrate_monges.py')
    )

