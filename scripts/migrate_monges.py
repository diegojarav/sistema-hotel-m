def create_backup(db_path: Path, backup_dir: Path) -> Path:
    """Create a timestamped backup of the database."""
    backup_dir.mkdir(parents=True, exist_ok=True)
    
    timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
    backup_name = f"hotel_pre_migration_{MIGRATION_NAME}_{timestamp}.db"
    backup_path = backup_dir / backup_name
    
    log_info(f"Creating backup: {backup_path}")
    
    # Use SQLite's backup API for safe copy
    source_conn = sqlite3.connect(str(db_path))
    backup_conn = sqlite3.connect(str(backup_path))
    
    source_conn.backup(backup_conn)
    
    backup_conn.close()
    source_conn.close()
    
    # Verify backup
    if backup_path.exists():
        size_mb = backup_path.stat().st_size / (1024 * 1024)
        log_success(f"Backup created: {backup_name} ({size_mb:.2f} MB)")
        return backup_path
    else:
        raise Exception("Backup creation failed!")

