def main():
    """Main entry point for the migration script."""
    import argparse
    
    parser = argparse.ArgumentParser(
        description="Migrate database for Los Monges MVP",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog="""
Examples:
  python migrate_monges.py              # Run migration with backup
  python migrate_monges.py --dry-run    # Show what would be done
  python migrate_monges.py --no-backup  # Skip backup (not recommended)
        """
    )
    parser.add_argument(
        '--dry-run', 
        action='store_true',
        help='Show what would be done without making changes'
    )
    parser.add_argument(
        '--no-backup',
        action='store_true',
        help='Skip automatic backup (not recommended)'
    )
    parser.add_argument(
        '--db-path',
        type=str,
        help='Custom database path (default: backend/hotel.db)'
    )
    
    args = parser.parse_args()
    
    # Determine database path
    db_path = Path(args.db_path) if args.db_path else DB_PATH
    
    print("")
    print("╔══════════════════════════════════════════════════════════╗")
    print("║     HOTEL MANAGEMENT SYSTEM - DATABASE MIGRATION         ║")
    print("║                  Los Monges MVP                          ║")
    print("╚══════════════════════════════════════════════════════════╝")
    print("")
    
    # Create backup unless skipped or dry run
    if not args.no_backup and not args.dry_run:
        try:
            log_section("PRE-MIGRATION BACKUP")
            backup_path = create_backup(db_path, BACKUP_DIR)
            log_info(f"Backup location: {backup_path}")
        except Exception as e:
            log_error(f"Backup failed: {e}")
            log_error("Aborting migration. Fix backup issue or use --no-backup (not recommended)")
            sys.exit(1)
    elif args.dry_run:
        log_info("Dry run mode - skipping backup")
    else:
        log_warning("Backup skipped by user request. Proceeding without backup.")
    
    # Run migration
    try:
        success = run_migration(db_path, dry_run=args.dry_run)
        sys.exit(0 if success else 1)
    except Exception as e:
        log_error(f"Migration failed with error: {e}")
        import traceback
        traceback.print_exc()
