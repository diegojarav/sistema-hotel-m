def add_properties_social_columns(conn: sqlite3.Connection, dry_run: bool = False):
    """Add social media columns to properties table."""
    log_info("Checking properties table for social media columns...")

    if not check_table_exists(conn, 'properties'):
        log_info("Properties table not found. Will be created with new schema.")
        return

    new_columns = [
        ('facebook_url', 'TEXT'),
        ('instagram_url', 'TEXT'),
    ]

    for column_name, column_type in new_columns:
        if check_column_exists(conn, 'properties', column_name):
            log_info(f"Column '{column_name}' already exists. Skipping.")
            continue

        if dry_run:
            log_info(f"[DRY RUN] Would add column: {column_name} {column_type}")
        else:
            try:
                conn.execute(f"ALTER TABLE properties ADD COLUMN {column_name} {column_type}")
                log_success(f"Added column: {column_name}")
            except sqlite3.OperationalError as e:
                if "duplicate column name" in str(e).lower():
                    log_info(f"Column '{column_name}' already exists.")
                else:
                    raise

