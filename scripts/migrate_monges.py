def run_migration(db_path: Path, dry_run: bool = False) -> bool:
    """
    Execute the full migration.
    
    Returns:
        True if migration was successful, False otherwise
    """
    log_section(f"DATABASE MIGRATION: {MIGRATION_NAME} v{MIGRATION_VERSION}")
    
    # Check database exists
    if not db_path.exists():
        log_error(f"Database not found: {db_path}")
        log_info("Please run the application first to create the initial database.")
        return False
    
    log_info(f"Database: {db_path}")
    log_info(f"Dry run: {dry_run}")
    
    # Connect to database
    conn = sqlite3.connect(str(db_path))
    conn.execute("PRAGMA foreign_keys = ON")
    
    try:
        # Check if migration already applied
        if check_migration_applied(conn, MIGRATION_VERSION, MIGRATION_NAME):
            log_warning(f"Migration {MIGRATION_NAME} v{MIGRATION_VERSION} already applied.")
            log_info("Use --force to re-run (not recommended).")
            return True
        
        # Start transaction
        if not dry_run:
            conn.execute("BEGIN TRANSACTION")
        
        # Step 1: Create new tables
        log_section("STEP 1: Creating New Tables")
        
        if dry_run:
            log_info("[DRY RUN] Would create the following tables:")
            tables = [
                'properties', 'buildings', 'room_categories', 'system_settings',
                'rooms_new', 'room_status_log', 'client_types', 'client_contracts',
                'pricing_seasons', 'price_calculations', 'ai_agent_permissions',
                'migration_history'
            ]
            for t in tables:
                exists = check_table_exists(conn, t)
                status = "EXISTS" if exists else "NEW"
                log_info(f"  - {t}: {status}")
        else:
            conn.executescript(SQL_CREATE_TABLES)
            log_success("New tables created successfully")
        
        # Step 2: Migrate rooms table
        log_section("STEP 2: Migrating Rooms Table")
        migrate_rooms_table(conn, dry_run)
        
        # Step 3: Add columns to reservations
        log_section("STEP 3: Updating Reservations Table")
        add_reservations_columns(conn, dry_run)

        # Step 3.5: Add social media columns to properties
        log_section("STEP 3.5: Adding Social Media Columns to Properties")
        add_properties_social_columns(conn, dry_run)

        # Step 4: Rename rooms_new to rooms (if rooms was migrated)
        log_section("STEP 4: Finalizing Room Structure")
        if check_table_exists(conn, 'rooms_new') and not check_table_exists(conn, 'rooms'):
            if dry_run:
                log_info("[DRY RUN] Would rename rooms_new to rooms")
            else:
                conn.execute("ALTER TABLE rooms_new RENAME TO rooms")
                log_success("Renamed rooms_new to rooms")
        elif check_table_exists(conn, 'rooms_new'):
            log_info("Both rooms and rooms_new exist. Manual review needed.")
        else:
            log_info("Room table structure already finalized.")
        
        # Step 5: Record migration
        log_section("STEP 5: Recording Migration")
        if not dry_run:
            record_migration(
                conn, 
                MIGRATION_VERSION, 
                MIGRATION_NAME,
                "Los Monges MVP: Added properties, categories, pricing, client types"
            )
            conn.execute("COMMIT")
            log_success("Migration recorded and committed")
        else:
            log_info("[DRY RUN] Would record migration in history")
        
        # Summary
        log_section("MIGRATION COMPLETE")
        
        if dry_run:
            log_warning("This was a DRY RUN. No changes were made.")
            log_info("Run without --dry-run to apply changes.")
        else:
            log_success(f"Migration {MIGRATION_NAME} v{MIGRATION_VERSION} applied successfully!")
            log_info("")
            log_info("Next steps:")
            log_info("  1. Run seed_monges.py to insert initial data")
            log_info("  2. Test the application")
            log_info("  3. If issues, restore from backup in /backups/")
        
        return True
        
    except Exception as e:
        log_error(f"Migration failed: {e}")
        if not dry_run:
            log_info("Rolling back changes...")
            conn.execute("ROLLBACK")
        raise
        
    finally:
        conn.close()

