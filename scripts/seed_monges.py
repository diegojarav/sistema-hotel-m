def check_data_exists(conn: sqlite3.Connection, table: str, id_column: str, id_value: str) -> bool:
    """Check if a record already exists."""
    cursor = conn.execute(f"SELECT 1 FROM {table} WHERE {id_column} = ?", (id_value,))
    return cursor.fetchone() is not None

def seed_property(conn: sqlite3.Connection, dry_run: bool = False):
    """Seed the property (hotel) record."""
    log_info(f"Seeding property: {PROPERTY_NAME}")
    
    if check_data_exists(conn, 'properties', 'id', PROPERTY_ID):
        log_warning(f"Property '{PROPERTY_ID}' already exists. Skipping.")
        return
    
    if dry_run:
        log_info(f"[DRY RUN] Would insert property: {PROPERTY_NAME}")
        return
    
    columns = ', '.join(PROPERTY_DATA.keys())
    placeholders = ', '.join(['?' for _ in PROPERTY_DATA])
    values = tuple(PROPERTY_DATA.values())
    
    conn.execute(f"INSERT INTO properties ({columns}) VALUES ({placeholders})", values)
    log_success(f"Property '{PROPERTY_NAME}' created")

