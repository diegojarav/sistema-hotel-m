def run_seed(db_path: Path, dry_run: bool = False, reset: bool = False) -> bool:
    """Execute the full seeding process."""
    log_section(f"SEEDING DATA: {PROPERTY_NAME}")
    
    if not db_path.exists():
        log_error(f"Database not found: {db_path}")
        log_info("Please run migrate_monges.py first.")
        return False
    
    log_info(f"Database: {db_path}")
    log_info(f"Dry run: {dry_run}")
    log_info(f"Reset: {reset}")
    
    conn = sqlite3.connect(str(db_path))
    conn.execute("PRAGMA foreign_keys = ON")
    
    try:
        # Start transaction
        if not dry_run:
            conn.execute("BEGIN TRANSACTION")
        
        # Reset if requested
        if reset:
            log_section("STEP 0: Reset Existing Data")
            reset_los_monges_data(conn, dry_run)
        
        # Seed in order (respecting foreign keys)
        log_section("STEP 1: Property")
        seed_property(conn, dry_run)
        
        # NOTE: buildings table removed from schema — skipping seed_building()

        log_section("STEP 2: Room Categories")
        seed_categories(conn, dry_run)
        
        log_section("STEP 3: Rooms")
        seed_rooms(conn, dry_run)

        log_section("STEP 4: Client Types")
        seed_client_types(conn, dry_run)

        log_section("STEP 5: Pricing Seasons")
        seed_pricing_seasons(conn, dry_run)

        log_section("STEP 6: AI Permissions")
        seed_ai_permissions(conn, dry_run)

        log_section("STEP 7: System Settings")
        seed_system_settings(conn, dry_run)
        
        # Commit
        if not dry_run:
            conn.execute("COMMIT")
        
        # Summary
        log_section("SEEDING COMPLETE")
        
        if dry_run:
            log_warning("This was a DRY RUN. No changes were made.")
            log_info("Run without --dry-run to apply changes.")
        else:
            log_success(f"Data for '{PROPERTY_NAME}' seeded successfully!")
            log_info("")
            log_info("Summary:")
            log_info(f"  - 1 property")
            log_info(f"  - {len(ROOM_CATEGORIES)} room categories")
            log_info(f"  - {sum(cat['qty'] for cat in ROOM_CATEGORIES)} rooms")
            log_info(f"  - {len(CLIENT_TYPES)} client types")
            log_info(f"  - {len(PRICING_SEASONS)} pricing seasons")
            log_info(f"  - {len(AI_PERMISSIONS)} AI permission sets")
            log_info(f"  - {len(SYSTEM_SETTINGS)} system settings")
            log_info("")
            log_info("Next steps:")
            log_info("  1. Update room quantities in this script if needed")
            log_info("  2. Run the application and test")
            log_info("  3. Add company contracts via the UI or another script")
        
        return True
        
    except Exception as e:
        log_error(f"Seeding failed: {e}")
        if not dry_run:
            log_info("Rolling back changes...")
            conn.execute("ROLLBACK")
        raise
        
    finally:
        conn.close()

