def seed_categories(conn: sqlite3.Connection, dry_run: bool = False):
    """Seed room categories."""
    log_info(f"Seeding {len(ROOM_CATEGORIES)} room categories...")
    
    inserted = 0
    skipped = 0
    updated = 0
    
    for cat in ROOM_CATEGORIES:
        if check_data_exists(conn, 'room_categories', 'id', cat['id']):
            log_info(f"  Category '{cat['name']}' already exists. Updating...")
            
            # Remove 'qty' which is not a database column
            cat_data = {k: v for k, v in cat.items() if k != 'qty'}
            
            # Construct UPDATE statement
            set_clause = ', '.join([f"{k} = ?" for k in cat_data.keys()])
            values = tuple(cat_data.values()) + (cat['id'],)
            
            conn.execute(f"UPDATE room_categories SET {set_clause} WHERE id = ?", values)
            log_success(f"  Category '{cat['name']}' updated")
            updated += 1
            inserted += 1 # Count as processed
            continue
        
        if dry_run:
            log_info(f"  [DRY RUN] Would insert: {cat['name']} - {cat['base_price']:,} Gs")
            inserted += 1
            continue
        
        # Remove 'qty' which is not a database column
        cat_data = {k: v for k, v in cat.items() if k != 'qty'}
        cat_data['property_id'] = PROPERTY_ID
        
        columns = ', '.join(cat_data.keys())
        placeholders = ', '.join(['?' for _ in cat_data])
        values = tuple(cat_data.values())
        
        conn.execute(f"INSERT INTO room_categories ({columns}) VALUES ({placeholders})", values)
        log_success(f"  Category '{cat['name']}' created ({cat['base_price']:,} Gs)")
        inserted += 1
    
    log_info(f"Categories: {inserted} processed (created/updated), {skipped} skipped")

def seed_rooms(conn: sqlite3.Connection, dry_run: bool = False):
    """Seed individual rooms based on category quantities."""
    log_info("Seeding rooms...")
    
    total_rooms = sum(cat['qty'] for cat in ROOM_CATEGORIES)
    log_info(f"Total rooms to create: {total_rooms}")
    
    inserted = 0
    skipped = 0
    room_counter = 1
    
    for cat in ROOM_CATEGORIES:
        category_id = cat['id']
        qty = cat['qty']
        
        for i in range(1, qty + 1):
            # Generate internal code like "DF-01" for Departamento Familiar room 1
            prefix = ''.join(word[0].upper() for word in cat['name'].split()[:2])
            internal_code = f"{prefix}-{i:02d}"
            room_id = f"{PROPERTY_ID}-room-{room_counter:03d}"
            
            if check_data_exists(conn, 'rooms', 'id', room_id):
                log_info(f"  Room '{room_id}' already exists. Skipping.")
                skipped += 1
                room_counter += 1
                continue
            
            if dry_run:
                log_info(f"  [DRY RUN] Would insert: {internal_code} ({cat['name']})")
                inserted += 1
                room_counter += 1
                continue
            
            room_data = {
                'id': room_id,
                'property_id': PROPERTY_ID,
                'category_id': category_id,
                'floor': 1 if room_counter <= 8 else 2,  # Split between floors
                'internal_code': internal_code,
                'status': 'available',
                'active': 1
            }
            
            columns = ', '.join(room_data.keys())
            placeholders = ', '.join(['?' for _ in room_data])
            values = tuple(room_data.values())
            
            conn.execute(f"INSERT INTO rooms ({columns}) VALUES ({placeholders})", values)
            inserted += 1
            room_counter += 1
    
    log_success(f"Rooms: {inserted} inserted, {skipped} skipped")

